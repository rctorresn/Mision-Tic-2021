'''Ejemplo condicional doble'''

#Entradas
calif1=int(input("Ingresa la calificación 1: "))
calif2=int(input("Ingresa la calificación 2: "))
calif3=int(input("Ingresa la calificación 3: "))

#Procedimiento
prom=(calif1+calif2+calif3)/3

#salidas
if prom>=70:
    print("Alumno aprobado")
else:
    print("Alumno reprobado")




